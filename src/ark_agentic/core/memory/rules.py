MEMORY_FILTER_RULES = """\
### 记录（长期有效的信息）
- 用户身份信息（姓名、职业、机构）
- 沟通风格偏好（"简洁点"、"用表格"）
- 持久业务偏好（"不考虑分红险"、"优先低风险产品"）
- 风险偏好（"保守型"、"能承受中等波动"）
- 用户明确要求记住的内容

### 不记录（短期/一次性信息）
- 单次操作决策（"这次取 5 万"、"帮我查一下 XX 产品"）
- 临时计算结果、市场行情数据、公开信息
- 寒暄、问候、闲聊
- 当前记忆中已有且未变化的内容

### 判断方法（正反例对）
- "这次取 5 万" → 单次操作 → 不记录
- "以后每次都取 5 万" → 持久偏好 → 记录
- "帮我查一下 XX 产品" → 单次查询 → 不记录
- "我不考虑分红险" → 持久排除偏好 → 记录
- "好啰嗦，简洁点" → 沟通风格 → 记录
- "今天大盘涨了" → 市场数据 → 不记录\
"""

